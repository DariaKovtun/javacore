package utils.runners;


public class Lesson3Runner {
   public static char floatToChar (float float1){
        char char1 = (char) float1;
        System.out.println("Input float value is " + (float) float1 + ". Output char value is " + (char) char1);
        return char1;
    }
    public static char intToChar (int int1){
        char char1 = (char) int1;
        System.out.println("Input float value is " + (char) int1 + ". Output char value is " + (char) char1);
        return char1;
    }
    public static int charToInt (char char1){
        int int1 = (char) char1;
        System.out.println("Input float value is " + (char) char1 + ". Output char value is " + (int) int1);
        return int1;
    }
}
